# Get a number of random samples from inside a given square.
function get_samples(square::Square, count)
	Ivl, Ivu, Ipl, Ipu = bounds(square)
	float_epsilon = eps() # Not to be confused with the epsilon variance of bounciness
	[(rand(Ivl:float_epsilon:Ivu), rand(Ipl:float_epsilon:Ipu)) for _ in 1:count]
end

# ╔═╡ 66ac2482-7aad-4721-85fd-080941928a54
# Get a number of random squares from a grid
function get_samples(grid::Grid, count)
	[Square(grid, rand(1:grid.v_count), rand(1:grid.p_count)) for _ in 1:count]
end

