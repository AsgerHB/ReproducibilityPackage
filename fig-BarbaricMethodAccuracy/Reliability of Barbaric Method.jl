# ╔═╡ 1c87cb42-1106-410a-84b7-027e83c8de07
function is_inside(squares::Vector{Square}, v, p)
	for square in squares
		if is_inside(square, v, p)
			return true
		end
	end
	false
end

# ╔═╡ 0f00e394-79de-4593-b1c0-48e1ae5f1363
function is_inside(square::Square, v, p)
	Ivl, Ivu, Ipl, Ipu = bounds(square)
	Ivl <= v < Ivu && Ipl <= p <= Ipu
end

# ╔═╡ 5ad7d844-3a3b-4e2a-97ed-1bb66d13ad40
function test_square(samples_per_axis, mechanics, square, sample_count, action)
	# Get set of reachable squares as computed by barbaric method
	computed_reachable = get_reachable_area(samples_per_axis, mechanics, square, action)
	computed_reachable = [Square(square.grid, iv, ip) 
							for (iv, ip) in computed_reachable]

	# Number of times the sample ended up outside the computed reachable area
	incorrect_count = 0

	# Get samples inside the square
	for (v, p) in get_samples(square, sample_count)
		# See where the samples end up next time-step
		v′, p′ = simulate_point(mechanics, v, p, action, unlucky=true)
		# Count if it ends up in a square outside the computed set of 
		# reachable squares
		if !is_inside(computed_reachable, v′, p′)
			if  (square.grid.v_min < v′ < square.grid.v_max  &&
				square.grid.p_min < p′ < square.grid.p_max)
				incorrect_count += 1
			end
		end
	end

	incorrect_count, sample_count
end

# ╔═╡ c8a5a0e6-b839-400b-90b0-171f4a8eb3a0
function test_grid(samples_per_axis, mechanics, grid; squares_to_test, samples_per_square)
	total_incorrect, total_sampled = 0, 0
	for square in get_samples(grid, squares_to_test)
		action = ["hit", "nohit"][rand(1:2)]
		incorrect_count, sample_count = test_square(samples_per_axis, mechanics, square, samples_per_square, action)
		total_incorrect += incorrect_count
		total_sampled += sample_count
	end
	total_incorrect, total_sampled
end

