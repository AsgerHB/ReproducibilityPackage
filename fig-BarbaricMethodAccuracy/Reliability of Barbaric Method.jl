md"""
# Reliability of the Barbaric Method

Code to check how reliably the barbaric method is able to predict the movements of a bouncing ball. 

A random initial square in the grid, and a random position and velocity is picked inside that square. The ball's position and velocity after one time-step is then computed using `simulate_point`. Lastly, it is checked whether this position was contained in one of the squares predicted by the barbaric reachability function. If it is, or if the ball ended up outside the grid, the reachability function was considered correct. Otherwise, an incorrect estimate is counted. 

This check is repeated a number of times for different values of samples per axis, $N$. Multiple points are picked inside the same square, because I think this will run faster.

